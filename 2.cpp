#include <stdio.h>

int main() {
    char c;
    int i;
    float f;
    double d;

    printf("Size of char: %d bytes\n", sizeof(c));
    printf("Size of int: %d bytes\n", sizeof(i));
    printf("Size of float: %d bytes\n", sizeof(f));
    printf("Size of double: %d bytes\n", sizeof(d));

    return 0;
}

